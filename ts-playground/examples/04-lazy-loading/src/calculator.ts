export class Calculator {

    add(first: number, second: number) {
        return first + second;
    }

    subtract(first: number, second: number) {
        return first - second;
    }

    multiply(first: number, second: number) {
        return first * second;
    }

    divide(first: number, second: number) {
        return first / second;
    }

}
